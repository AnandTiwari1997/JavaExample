package com.mavendemo.matrix;

public class IndexWrapper {
	
	private int i;
	private int j;
	
	
	public IndexWrapper(int i, int j) {
		super();
		this.i = i;
		this.j = j;
	}


	public int getI() {
		return i;
	}


	public void setI(int i) {
		this.i = i;
	}


	public int getJ() {
		return j;
	}


	public void setJ(int j) {
		this.j = j;
	}

}
